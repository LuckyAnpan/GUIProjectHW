        public MainWindow()
        {
            InitializeComponent();
        }

        private void btnCalculate_Click(object sender, RoutedEventArgs e)
        {
            int intIncome = int.Parse(txtIncome.Text);
            int intExpenses = int.Parse(txtExpenses.Text);
            int intPrice = int.Parse(txtPrice.Text);

            int result = intPrice / (intIncome - intExpenses);

            txtDay.Text = result.ToString();
        }
    }
